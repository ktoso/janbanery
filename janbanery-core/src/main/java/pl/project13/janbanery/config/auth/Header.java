package pl.project13.janbanery.config.auth;

/**
 * @author Konrad Malawski
 */
public class Header {
  private String key;
  private String value;

  public Header(String key, String value) {
    this.key = key;
    this.value = value;
  }

  public String getKey() {
    return key;
  }

  public String getValue() {
    return value;
  }

  public String toString() {
    return "Header{" +
        "key='" + key + '\'' +
        ", value='" + value + '\'' +
        '}';
  }
}
